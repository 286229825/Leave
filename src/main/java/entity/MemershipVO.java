package entity;

public class MemershipVO extends Memership{
	private String first_;
	private String last_;
	private String pwd_;
	private String email_;
	private String name_;
	public String getFirst_() {
		return first_;
	}
	public void setFirst_(String first_) {
		this.first_ = first_;
	}
	public String getLast_() {
		return last_;
	}
	public void setLast_(String last_) {
		this.last_ = last_;
	}
	public String getPwd_() {
		return pwd_;
	}
	public void setPwd_(String pwd_) {
		this.pwd_ = pwd_;
	}
	public String getEmail_() {
		return email_;
	}
	public void setEmail_(String email_) {
		this.email_ = email_;
	}
	public String getName_() {
		return name_;
	}
	public void setName_(String name_) {
		this.name_ = name_;
	}
	
}
